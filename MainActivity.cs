                        Fm4.LoadCustomUrl(JavascriptCommands._jsShowPageBar);
                        break;
                }
                if (AppSettings.DlFabShowSetting != "never" ||
                    (AppSettings.DlFabShowSetting == "onpress" && TabStates.Tab3.VideoDownloaderViewEnabled))
                {
                    ViewHelpers.Main.DownloadFAB.Show();
                }
                AppState.Display.Horizontal = false;
                _window.ClearFlags(_winflagfullscreen);
                _window.AddFlags(_winflagnotfullscreen);
                CustomOnTouch();
            }

            if (!AppSettings.HideHorizontalNavBar || newConfig.Orientation == Orientation.Portrait)
            {
                NavTimeout = false;
            }

            //app seems to be lagging ontouch so removing the touch listener when app is portrait
            Fm0.CustomSetTouchListener(AppState.Display.Horizontal);
            Fm1.CustomSetTouchListener(AppState.Display.Horizontal);
            Fm2.CustomSetTouchListener(AppState.Display.Horizontal);
            Fm3.CustomSetTouchListener(AppState.Display.Horizontal);
            Fm4.CustomSetTouchListener(AppState.Display.Horizontal);
        }

        /// <summary>
        /// returns a Drawable icon to be used for tab detail changing
        /// takes string arguments like "Home" "Subs" "Feed" "MyChannel" "Settings"
        /// </summary>
        /// <param name="tabString"></param>
        /// <returns></returns>
        public static Drawable GetTabIconFromString(string tabString)
        {
            Drawable icon;

            switch (tabString)
            {
                case "Home":
                    icon = Main.GetDrawable(Resource.Drawable.tab_home);
                    return icon;
                case "Subs":
                    icon = Main.GetDrawable(Resource.Drawable.tab_subs);
                    return icon;
                case "Feed":
                    icon = Main.GetDrawable(Resource.Drawable.tab_playlists);
                    return icon;
                case "MyChannel":
                    icon = Main.GetDrawable(Resource.Drawable.tab_mychannel);
                    return icon;
                case "Settings":
                    icon = Main.GetDrawable(Resource.Drawable.tab_settings);
                    return icon;
                case "Playlists":
                    icon = Main.GetDrawable(Resource.Drawable.tab_playlists);
                    return icon;
                case "WatchL8r":
                    icon = Main.GetDrawable(Resource.Drawable.tab_playlists);
                    return icon;
                case "Explore":
                    icon = Main.GetDrawable(Resource.Drawable.tab_home);
                    return icon;
            }
            return Main.GetDrawable(Resource.Drawable.tab_home);
        }


        protected override void OnPause()
        {
            base.OnPause();
            try
            {
                ExtStickyService.StartVideoInBkgrd(MainActivity.ViewPager.CurrentItem);
            }
            catch { }
        }

        protected override void OnResume()
        {
            AppState.MediaPlayback.UserRequestedBackgroundPlayback = false;
            if (!TabStates.Tab3.VideoDownloaderViewEnabled)
            {
                if (AppSettings.DlFabShowSetting != "always")
                {
                    ViewHelpers.Main.DownloadFAB.Visibility = ViewStates.Gone;
                }
                else { ViewHelpers.Main.DownloadFAB.Visibility = ViewStates.Visible; }
            }
            else if (TabStates.Tab3.VideoDownloaderViewEnabled)
            {
                if (AppSettings.DlFabShowSetting != "never")
                {
                    ViewHelpers.Main.DownloadFAB.Visibility = ViewStates.Visible;
                }
                else { ViewHelpers.Main.DownloadFAB.Visibility = ViewStates.Gone; }
            }

        //    if (Intent != null)
        //    {

        //    }
            try {
                IntentFilter filter = new IntentFilter(Intent.ActionHeadsetPlug);
                RegisterReceiver(ForegroundReceiver, filter);
            }
            catch(Exception ex)
            {
                Console.WriteLine(ex.Message);
            }

            base.OnResume();
        }
        
        protected override void OnDestroy()
        {
            try
            {
                //UnregisterReceiver(ForegroundReceiver);
                //
                AppState.NotificationStartedApp = false;
            }
            catch { }
            ViewPager.PageSelected -= ViewPager_PageSelected;
            NavigationView.NavigationItemSelected -= NavigationView_NavigationItemSelected;
            ExtStickyService.ExternalStopForeground();
            base.OnDestroy();
        }

